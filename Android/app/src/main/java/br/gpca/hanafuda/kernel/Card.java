package br.gpca.hanafuda.kernel;

public class Card {
    public int ID;
    public int family;
    public int ken;

    public int getID() {
        return ID;
    }

    public void setID(int iD) {
        ID = iD;
    }

    public int getFamily() {
        return family;
    }

    public void setFamily(int family) {
        this.family = family;
    }

    public int getKen() {
        return ken;
    }

    public void setKen(int ken) {
        this.ken = ken;
    }

}

